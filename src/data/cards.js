// Função para embaralhar cartas
export const shuffleArray = (array) => {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
};

// Função para distribuir cartas iniciais
export const dealInitialCards = (players, cardsPerPlayer = 10) => {
  const shuffledAnswers = shuffleArray(answerCards);
  const playerHands = {};
  
  players.forEach((player, index) => {
    const startIndex = index * cardsPerPlayer;
    const endIndex = startIndex + cardsPerPlayer;
    playerHands[player.id] = shuffledAnswers.slice(startIndex, endIndex);
  });
  
  return {
    playerHands,
    remainingCards: shuffledAnswers.slice(players.length * cardsPerPlayer)
  };
};

// Função para pegar próxima carta de pergunta
